// --- gradient helpers -----------------------------------------------------

func parseHex(s string) (r, g, b int) {
	s = strings.TrimPrefix(s, "#")
	if len(s) != 6 {
		return 200, 200, 200
	}
	v, _ := strconv.ParseInt(s, 16, 32)
	return int(v>>16) & 0xff, int(v>>8) & 0xff, int(v) & 0xff
}

func mix(a, b string, t float64) lipgloss.Color {
	ar, ag, ab := parseHex(a)
	br, bg, bb := parseHex(b)
	lp := func(x, y int) int { return int(float64(x) + float64(y-x)*t) }
	return lipgloss.Color(fmt.Sprintf("#%02x%02x%02x", lp(ar, br), lp(ag, bg), lp(ab, bb)))
}

// buildRamp makes a seamless cyclic gradient of n colors through the anchors.
func buildRamp(anchors []string, n int) []lipgloss.Color {
	if len(anchors) < 2 {
		anchors = []string{"#89b4fa", "#cba6f7"}
	}
	out := make([]lipgloss.Color, n)
	for i := 0; i < n; i++ {
		p := float64(i) / float64(n) * float64(len(anchors))
		k := int(p) % len(anchors)
		k2 := (k + 1) % len(anchors)
		out[i] = mix(anchors[k], anchors[k2], p-float64(int(p)))
	}
	return out
}

// gradientText paints each rune along the animated gradient ramp.
func gradientText(s string, frame int) string {
	if len(gradRamp) == 0 {
		return s
	}
	var b strings.Builder
	for i, r := range []rune(s) {
		c := gradRamp[((i*2+frame)%len(gradRamp)+len(gradRamp))%len(gradRamp)]
		b.WriteString(lipgloss.NewStyle().Foreground(c).Bold(true).Render(string(r)))
	}
	return b.String()
}

