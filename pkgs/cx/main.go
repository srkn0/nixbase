// updateDialog drives any dialog generically: arrows move focus, Enter on a
// button runs its action, typing edits the focused text field.
func (m *model) updateDialog(msg tea.KeyMsg) (tea.Model, tea.Cmd) {
	d := &m.dlg
	f := &d.fields[d.focus]
	switch msg.String() {
	case "esc", "ctrl+c":
		m.dlg = dialog{}
	case "up", "shift+tab":
		m.dlgFocusMove(-1)
	case "down", "tab":
		m.dlgFocusMove(1)
	case "left":
		if f.kind == fButton {
			m.dlgFocusMove(-1)
		}
	case "right":
		if f.kind == fButton {
			m.dlgFocusMove(1)
		}
	case "enter":
		if f.kind == fButton {
			m.dlgAction(f.action)
		} else {
			m.dlgFocusMove(1)
		}
	case "backspace":
		if f.kind == fText && f.value != "" {
			f.value = f.value[:len(f.value)-1]
		}
	default:
		if f.kind == fText && (msg.Type == tea.KeyRunes || msg.Type == tea.KeySpace) {
			f.value += string(msg.Runes)
		}
	}
	return m, nil
}

func (m *model) dlgAction(action string) {
	d := m.dlg
	node := d.node
	m.dlg = dialog{} // close; the operation sets a status message
	switch action {
	case "cancel":
	case "save-alias":
		m.setAlias(node, d.fields[0].value)
	case "create":
		m.createCommand(node, d.fields[0].value, d.fields[1].value, d.fields[2].value, d.fields[3].value)
	case "save-cmd":
		m.editCommand(node, d.fields[0].value, d.fields[1].value, d.fields[2].value, d.fields[3].value)
	case "create-cat":
		m.createCategory(node, d.fields[0].value, d.fields[1].value, d.fields[2].value)
	case "save-cat":
		m.editCategory(node, d.fields[0].value, d.fields[1].value, d.fields[2].value)
	case "delete":
		m.deleteNode(node)
	}
}

