func key(k, label string) string { return stKey.Render(k) + " " + stHelp.Render(label) }

func (m *model) breadcrumb() string {
	crumb := ""
	for i, n := range m.stack {
		name := n.Name
		if i == 0 {
			name = "home"
		}
		st := stCrumb
		if i == len(m.stack)-1 {
			st = stCrumbCur
		}
		if i > 0 {
			crumb += stSep.Render(" ")
		}
		crumb += st.Render(name)
	}
	if m.filter != "" {
		crumb += "   " + stPrompt.Render(" "+m.filter)
	}
	return crumb
}

func (m *model) View() string {
	// header: animated logo + subtitle + breadcrumb
	lg := m.logo()
	sub := stDesc.Render("command library") + stSep.Render(" · ") + stDesc.Render("shortcut manager")
	header := lg[0] + "\n" +
		lg[1] + "   " + sub + "\n" +
		lg[2] + "   " + m.breadcrumb()

	listW := m.w/2 - 5
	if listW < 20 {
		listW = 20
	}
	prevW := m.w - listW - 9
	if prevW < 16 {
		prevW = 16
	}
	innerH := m.h - 9
	if innerH < 3 {
		innerH = 3
	}

	start := 0
	if m.cursor >= innerH {
		start = m.cursor - innerH + 1
	}
	end := start + innerH
	if end > len(m.rows) {
		end = len(m.rows)
	}

	var b strings.Builder
	if len(m.rows) == 0 {
		b.WriteString(stDesc.Render("(nichts gefunden)"))
	}
	for i := start; i < end; i++ {
		r := m.rows[i]
		g := r.node.glyph()
		label := r.node.Name
		if m.filter != "" && r.path != "" {
			label = r.path
		}
		badge := ""
		if r.node.isLeaf() && r.node.Alias != "" {
			badge = " [" + r.node.Alias + "]"
		}
		if i == m.cursor {
			line := " " + g + "  " + label + badge
			if r.isGroup {
				line += fmt.Sprintf("  (%d)", len(r.node.Items))
			}
			b.WriteString(stSel.Width(listW).Render(truncate(line, listW-1)))
		} else {
			icon, nst := stLIcon, stName
			if r.isGroup {
				icon, nst = stGIcon, stGroup
			}
			avail := listW - 9 - len([]rune(badge))
			seg := " " + icon.Render(g) + "  " + nst.Render(truncate(label, avail))
			switch {
			case r.isGroup:
				seg += stCount.Render(fmt.Sprintf("  (%d)", len(r.node.Items)))
			case badge != "":
				seg += stCur.Render(badge)
			}
			b.WriteString(seg)
		}
		if i < end-1 {
			b.WriteByte('\n')
		}
	}
	listPanel := stListBox.Width(listW).Height(innerH).Render(b.String())

	var pb strings.Builder
	if len(m.rows) > 0 {
		sel := m.rows[m.cursor].node
		iconSt := stLIcon
		if !sel.isLeaf() {
			iconSt = stGIcon
		}
		pb.WriteString(iconSt.Render(sel.glyph()) + "  " + stTitleBig.Render(sel.Name))
		if !sel.isLeaf() {
			pb.WriteString("  " + stCount.Render(fmt.Sprintf("%d items", len(sel.Items))))
		}
		if sel.isLeaf() && sel.Alias != "" {
			pb.WriteString("  " + stCur.Render("["+sel.Alias+"]"))
		}
		pb.WriteString("\n\n")
		if sel.Desc != "" {
			pb.WriteString(stDesc.Width(prevW).Render(sel.Desc) + "\n")
		}
		if sel.isLeaf() {
			pb.WriteString("\n" + stCmd.Render("❯") + " " + stCmd.Width(prevW-3).Render(sel.Cmd))
			if sel.Alias != "" {
				pb.WriteString("\n\n" + stDesc.Render("alias ") + stCur.Render(sel.Alias))
			}
		}
	}
	prevPanel := stBox.Width(prevW).Height(innerH).Render(pb.String())

	body := lipgloss.JoinHorizontal(lipgloss.Top, listPanel, " ", prevPanel)

	// footer: status / help
	var footer string
	switch {
	case m.status != "":
		footer = stOK.Render(m.status)
	default:
		footer = strings.Join([]string{
			key("↑↓", "move"), key("→", "open"), key("←", "back"), key("type", "search"),
			key("^n", "cmd"), key("^g", "cat"), key("^e", "edit"), key("^a", "alias"), key("^x", "del"),
			key("⏎", "pick"), key("esc", "quit"),
		}, stSep.Render(" · "))
	}

	base := header + "\n\n" + body + "\n" + footer
	if m.dlg.kind != dlgNone {
		return overlayCenter(base, m.renderDialog(), m.w)
	}
	return base
}

// renderDialog builds the active modal popup box.
func (m *model) renderDialog() string {
	d := m.dlg
	w := 60
	if w > m.w-6 {
		w = m.w - 6
	}
	if w < 28 {
		w = 28
	}
	var c strings.Builder
	c.WriteString(stTitleBig.Render(" "+d.title) + "\n\n")
	for i, f := range d.fields {
		focused := i == d.focus
		switch f.kind {
		case fText:
			if focused {
				plain := " " + f.label + ": " + f.value + "▏"
				c.WriteString(stSel.Width(w).Render(truncate(plain, w-1)))
			} else {
				c.WriteString(" " + stDesc.Render(f.label+": ") +
					stName.Render(truncate(f.value, w-len(f.label)-4)))
			}
			c.WriteByte('\n')
			if f.alias {
				c.WriteString("   " + m.aliasHint(f.value) + "\n")
			}
		case fButton:
			if focused {
				c.WriteString(stSel.Width(w).Render(" ▸ " + f.label))
			} else {
				c.WriteString("   " + stKey.Render(f.label))
			}
			c.WriteByte('\n')
		}
	}
	c.WriteString("\n" + stHelp.Render("↑↓ move · ⏎ activate · esc cancel"))
	return lipgloss.NewStyle().
		Border(lipgloss.RoundedBorder()).BorderForeground(stCur.GetForeground()).
		Padding(1, 2).Width(w).Render(c.String())
}

func (m *model) aliasHint(val string) string {
	switch m.nameStatus(val, m.dlg.node) {
	case "current":
		return stDesc.Render("↺ unchanged")
	case "dup":
		return stWarn.Render("⚠ used by another command")
	case "conflict":
		return stWarn.Render("⚠ also an alias outside cx")
	case "new":
		return stOK.Render("✓ free")
	default:
		if strings.TrimSpace(val) != "" {
			return stWarn.Render("✗ invalid")
		}
		return stDesc.Render("optional")
	}
}

// overlayCenter splices a box centered over the base view (nvim-style popup).
// It centers within the base's OWN height and never grows the frame — padding
// out to the full terminal height would overflow it, scroll, and drop the
// footer line when the dialog closes.
func overlayCenter(base, box string, w int) string {
	if w <= 0 {
		return box
	}
	bl := strings.Split(base, "\n")
	boxLines := strings.Split(box, "\n")
	top := (len(bl) - len(boxLines)) / 2
	if top < 0 {
		// base shorter than the box: grow just enough, no more
		for len(bl) < len(boxLines) {
			bl = append(bl, "")
		}
		top = 0
	}
	left := (w - lipgloss.Width(box)) / 2
	if left < 0 {
		left = 0
	}
	pad := strings.Repeat(" ", left)
	for i, ln := range boxLines {
		if y := top + i; y >= 0 && y < len(bl) {
			bl[y] = pad + ln
		}
	}
	return strings.Join(bl, "\n")
}

func truncate(s string, w int) string {
	r := []rune(s)
	if len(r) <= w || w <= 1 {
		return s
	}
	return string(r[:w-1]) + "…"
}

